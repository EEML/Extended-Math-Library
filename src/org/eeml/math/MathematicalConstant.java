package org.eeml.math;

/**
 * This class mainly contains some general mathematical constants.
 * When solving math problems, those constants might help.
 * Maintained by <a href="http://github.com/eeml">EEML</a>. see more on <code>eeml.github.io</code>
 *
 * @author EULIR
 * @version v1.0.0
 * @see org.eeml.math
 */
public class MathematicalConstant
{
	/**
	 * 0 (zero) is both a number and the numerical digit
	 * used to represent that number in numerals.
	 */
	public static final int ZERO = 0;

	/**
	 * 1 (one, also called unit, unity, and (multiplicative)
	 * identity) is a number, numeral, and glyph. It
	 * represents a single entity, the unit of counting
	 * or measurement.
	 */
	public static final int UNITY = 1;

	/**
	 * The number π is a mathematical constant. Originally
	 * defined as the ratio of a circle's circumference to
	 * its diameter, it now has various equivalent
	 * definitions and appears in many formulas in all
	 * areas of mathematics and physics.
	 */
	public static final double PI = 3.141592653589793;

	/**
	 * The number e is a mathematical constant, approximately
	 * equal to 2.71828, which appears in many different
	 * settings throughout mathematics. It was discovered by
	 * the Swiss mathematician Jacob Bernoulli while studying
	 * compound interest, where e arises as the limit of
	 * (1 + 1/n)^n as n approaches infinity. The number e can
	 * also be calculated as the sum of the infinite series.
	 */
	public static final double E = 2.718281828459045;

	/**
	 * The square root of 2, or the (1/2)th power of 2,
	 * written in mathematics as √2 or 2​^1/2, is the positive
	 * algebraic number that, when multiplied by itself,
	 * gives the number 2.
	 */
	public static final double PYTHAGORAS_CONSTANT = 1.414213562373095;

	/**
	 * The square root of 3 is the positive real number that,
	 * when multiplied by itself, gives the number 3. It is
	 * more precisely called the principal square root of 3,
	 * to distinguish it from the negative number with the
	 * same property. The square root of 3 is an irrational
	 * number. It is also known as Theodorus' constant, named
	 * after Theodorus of Cyrene, who proved its irrationality.
	 */
	public static final double THEODORUS_CONSTANT = 1.732050807568877;

	/**
	 * The Euler–Mascheroni constant (also called Euler's
	 * constant) is a mathematical constant recurring in
	 * analysis and number theory, usually denoted by the
	 * lowercase Greek letter gamma (γ).
	 */
	public static final double EULER_MASCHERONO_CONSTANT = 0.577215664901532;

	/**
	 * In mathematics, two quantities are in the golden ratio
	 * if their ratio is the same as the ratio of their sum
	 * to the larger of the two quantities. The figure on the
	 * right illustrates the geometric relationship.
	 */
	public static final double GOLDEN_RATIO = 1.618033988749894;

	/**
	 * The Meissel–Mertens constant (named after Ernst Meissel
	 * and Franz Mertens), also referred to as Mertens constant,
	 * Kronecker's constant, Hadamard–de la Vallée-Poussin
	 * constant or the prime reciprocal constant, is a
	 * mathematical constant in number theory, defined as the
	 * limiting difference between the harmonic series summed
	 * only over the primes and the natural logarithm of the
	 * natural logarithm.
	 */
	public static final double MEISSEL_MERTEBS_CONSTANT = 0.261497212847642;

	/**
	 * Bernstein's constant, usually denoted by the Greek
	 * letter β (beta), is a mathematical constant named after
	 * Sergei Natanovich Bernstein.
	 */
	public static final double BERNASTEIN_CONSTANT = 0.280169499023869;

	/**
	 * In mathematics, the Gauss–Kuzmin–Wirsing operator is
	 * the transfer operator of the Gauss map.
	 */
	public static final double GAUSS_KUZMIN_WIRSING_CONSTANT = 0.303663002898732;

	/**
	 * The Hafner–Sarnak–McCurley constant is a mathematical
	 * constant representing the probability that the
	 * determinants of two randomly chosen square integer
	 * matrices will be relatively prime.
	 */
	public static final double HAFNER_SARNAK_MCCURLEY_CONSTANT = 0.353236371854995;

	/**
	 * The omega constant is a mathematical constant which
	 * is the value of W(1) where W is Lambert's W function.
	 * The name is derived from the alternate name for
	 * Lambert's W function, the omega function.
	 */
	public static final double OMEGA_CONSTANT = 0.567143290409783;

	/**
	 * A twin prime is a prime number that is either 2 less
	 * or 2 more than another prime number—for example,
	 * either member of the twin prime pair (41, 43). In
	 * other words, a twin prime is a prime that has a prime
	 * gap of two. Sometimes the term twin prime is used for
	 * a pair of twin primes; an alternative name for this
	 * is prime twin or prime pair.
	 */
	public static final double TWIN_PRIME_CONSTANT = 0.660161815846869;

	/**
	 * By the sum of two squares theorem, the numbers that can
	 * be expressed as a sum of two squares of integers are the
	 * ones for which each prime number congruent to 3 mod 4
	 * appears with an even exponent in the prime factorization.
	 */
	public static final double LANDAU_PAMANUJAN_CONSTANT = 0.764223653589220;

	/**
	 * In mathematics, Catalan's constant G, which appears in
	 * combinatorics, is defined by
	 * G = β(2) = 1/1^2 - 1/3^2 + 1/5^2 - 1/7^2 + 1/9^2 - ...
	 * where β is the Dirichlet beta function.
	 */
	public static final double CATALAN_CONSTANT = 0.915965594177219;

	/**
	 * In number theory, Mills' constant is defined as the
	 * smallest positive real number A such that the floor
	 * function of the double exponential function
	 * ⌊A^3n⌋
	 * is a prime number, for all natural numbers n.
	 */
	public static final double MILLS_CONSTANT = 1.306377883863080;

	/**
	 * In mathematics, the plastic number(also known as
	 * the plastic constant, the minimal Pisot number, the
	 * platin number, Siegel's number or, in French, le nombre
	 * radiant) is a mathematical constant which is the unique
	 * real solution of the cubic equation
	 * x^3 = x + 1
	 */
	public static final double PLASTIC_CONSTANT = 1.324717957244746;

	/**
	 * In mathematics, the Ramanujan–Soldner constant (also called
	 * the Soldner constant) is a mathematical constant defined as
	 * the unique positive zero of the logarithmic integral
	 * function. It is named after Srinivasa Ramanujan and Johann
	 * Georg von Soldner.
	 */
	public static final double RAMANUJAN_SOLDNER_CONSTANT = 1.451369234883381;

	/**
	 * In mathematics, specifically bifurcation theory, the Feigenbaum
	 * constants are two mathematical constants which both express ratios
	 * in a bifurcation diagram for a non-linear map. They are named
	 * after the mathematician Mitchell Feigenbaum.
	 */
	public static final double FERGENBAUM_CONSTANT = 2.502907875095892;

	/**
	 *The reciprocal Fibonacci constant, or ψ, is defined as the sum
	 * of the reciprocals of the Fibonacci numbers:
	 * ψ = 1/1 + 1/1 + 1/2 + 1/3 + 1/5 + 1/8 + 1/13 + 1/21 + 1/34 + ...
	 * The ratio of successive terms in this sum tends to the
	 * reciprocal of the golden ratio. Since this is less than 1, the
	 * ratio test shows that the sum converges.
	 */
	public static final double RECIPROCAL_FIBONACCI_CONSTANT = 3.359885666243177;

	public MathematicalConstant()
	{}
}
